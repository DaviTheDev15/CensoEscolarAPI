CREATE TABLE IF NOT EXISTS Instituicoes (
    NU_ANO_CENSO INTEGER,
    NO_REGIAO TEXT,
    CO_REGIAO INTEGER,
    NO_UF TEXT,
    SG_UF TEXT,
    CO_UF INTEGER,
    NO_MUNICIPIO TEXT,
    CO_MUNICIPIO INTEGER,
    CO_MESORREGIAO INTEGER,
    CO_MICRORREGIAO INTEGER,
    NO_ENTIDADE TEXT,
    CO_ENTIDADE BIGINT NOT NULL,
    QT_MAT_BAS INTEGER,
    QT_MAT_EJA INTEGER,
    QT_MAT_ESP INTEGER,
    QT_MAT_FUND INTEGER,
    QT_MAT_INF INTEGER,
    QT_MAT_MED INTEGER,
    QT_MAT_PROF INTEGER,
    PRIMARY KEY (NU_ANO_CENSO, CO_ENTIDADE),
    CONSTRAINT fk_CO_UF FOREIGN KEY (CO_UF) REFERENCES UFs(CO_UF),
    CONSTRAINT fk_CO_MUNICIPIO FOREIGN KEY (CO_MUNICIPIO) REFERENCES Municipios(CO_MUNICIPIO),
    CONSTRAINT fk_CO_MESORREGIAO FOREIGN KEY (CO_MESORREGIAO) REFERENCES Mesorregioes(CO_MESORREGIAO),
    CONSTRAINT fk_CO_MICRORREGIAO FOREIGN KEY (CO_MICRORREGIAO) REFERENCES microrregioes(CO_MICRORREGIAO)
);
